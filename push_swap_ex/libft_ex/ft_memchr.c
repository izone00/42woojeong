#include "libft.h"

void	*ft_memchr(const void *str, int ch, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		if (((unsigned char *)str)[i] == (unsigned char)ch)
			return ((unsigned char *)(str + i));
		i++;
	}
	return (0);
}
